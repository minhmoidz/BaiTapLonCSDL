-- Tạo bảng Chuyến đi

CREATE TABLE ChuyenDi
(
    ID_ChuyenDi INT PRIMARY KEY,
    ID_LichTrinh INT,
    ID_TaiXe INT,
    NgayDi DATE,
    ID_KhachHang INTEGER,
    SoLuongHanhKhach INT,
    FOREIGN KEY (ID_LichTrinh) REFERENCES LichTrinh(ID_LichTrinh),
    FOREIGN KEY (ID_TaiXe) REFERENCES TaiXe(ID_TaiXe)
);

